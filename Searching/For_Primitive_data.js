/// searching in primitive data

let array = [1,3,5,7,8,9];

// using indexof method
if(array.indexOf(9) != -1){
    console.log("Element found");
}else{
    console.log("Not  found");
}

// using includes method
if(array.includes(9)=== true){
    console.log("Element found");
}else{
    console.log("Not  found");
}

